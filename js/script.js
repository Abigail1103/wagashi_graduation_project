function navigateToSpringPage() {
    // 添加一个 CSS 类，以触发淡出过渡效果
    document.body.classList.add('fade-out');

    // 在过渡结束后，页面导航到 spring.html
    setTimeout(function() {
        window.location.href = "spring.html";
    }, 500); // 500 毫秒是过渡效果的时间
}
